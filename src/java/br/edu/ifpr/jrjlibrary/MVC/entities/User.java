package br.edu.ifpr.jrjlibrary.MVC.entities;

/**
 *
 * @author jvolima
 */
public class User {
    String name;
    String email;
    String password;
    String course;
    int age;
    
    public User(String name, String email, String password, String course, int age) {
        this.name = name;
        this.email = email;
        this.password = password;
        this.course = course;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
